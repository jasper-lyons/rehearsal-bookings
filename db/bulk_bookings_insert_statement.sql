    bookings(
        created_at,
        updated_at,
        customer_name,
        customer_email,
        customer_phone,
        room_name,
        start_time,
        end_time,
        notes,
        type,
        status,
        expiration,
        price,
        transaction_id,
        cymbals,
        discount_amount,
        booking_notes,
        payment_method
    )                                                         
