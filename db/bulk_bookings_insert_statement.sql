    strftime('%Y-%m-%dT%H:%M:%SZ',created_at),
    strftime('%Y-%m-%dT%H:%M:%SZ',updated_at),
    customer_name,
    customer_email,
    customer_phone,
    room_name,
    strftime('%Y-%m-%dT%H:%M:%SZ',start_time),
    strftime('%Y-%m-%dT%H:%M:%SZ',end_time),
    notes,
    CAST(type AS VARCHAR(100)),
    CAST(status AS VARCHAR(100)),
